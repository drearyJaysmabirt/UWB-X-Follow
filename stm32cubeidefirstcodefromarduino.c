void requestDistances() {
  char *cmd = "les\n";
  HAL_UART_Transmit(&huart7, (uint8_t*)cmd, strlen(cmd), 10);
  HAL_UART_Transmit(&huart2, (uint8_t*)cmd, strlen(cmd), 10);
  HAL_UART_Transmit(&huart6, (uint8_t*)cmd, strlen(cmd), 10);
}

float parseDistance(char *buf) {
  char *equals = strchr(buf, '=');
  if (equals) return atof(equals + 1);
  return 0.0f;
}

void processIncomingData() {
  if (HAL_UART_Receive(&huart7, (uint8_t*)rx1, sizeof(rx1), 10) == HAL_OK) {
    float parsed = parseDistance(rx1);
    if (parsed > 0) { uwb_triangle.d1 = parsed; d1_updated = true; }
  }
  if (HAL_UART_Receive(&huart2, (uint8_t*)rx2, sizeof(rx2), 10) == HAL_OK) {
    float parsed = parseDistance(rx2);
    if (parsed > 0) { uwb_triangle.d2 = parsed; d2_updated = true; }
  }
  if (HAL_UART_Receive(&huart6, (uint8_t*)rx3, sizeof(rx3), 10) == HAL_OK) {
    float parsed = parseDistance(rx3);
    if (parsed > 0) { uwb_triangle.d3 = parsed; d3_updated = true; }
  }
}

void calculateXYPosition() {
  float x1 = -0.20, y1 = 0.0;
  float x2 =  0.20, y2 = 0.0;
  float x3 =  0.0,  y3 = -0.45;

  float r1 = uwb_triangle.d1;
  float r2 = uwb_triangle.d2;
  float r3 = uwb_triangle.d3;

  float A = 2 * (x2 - x1);
  float B = 2 * (y2 - y1);
  float C = r1*r1 - r2*r2 - x1*x1 + x2*x2 - y1*y1 + y2*y2;

  float D = 2 * (x3 - x2);
  float E = 2 * (y3 - y2);
  float F = r2*r2 - r3*r3 - x2*x2 + x3*x3 - y2*y2 + y3*y3;

  float denominator = A * E - B * D;
  if (fabs(denominator) < 1e-6) {
    uwb_triangle.x = uwb_triangle.y = 0;
    printf("Trilateration failed (denominator too small)\r\n");
    return;
  }

  uwb_triangle.x = (C * E - F * B) / denominator;
  uwb_triangle.y = (A * F - D * C) / denominator;
}

float calculateHeading() {
  float dx = uwb_triangle.x;
  float dy = uwb_triangle.y;
  float heading_rad = atan2(dx, dy);
  float heading_deg = heading_rad * 180.0f / M_PI;
  if (heading_deg < 0) heading_deg += 360.0f;
  return heading_deg;
}

void displayResults() {
  printf("\033[2J\033[H");
  printf("=== UWB TRIANGULATION SYSTEM ===\r\n");
  printf("DISTANCES:\r\n  D1: %.3f\r\n  D2: %.3f\r\n  D3: %.3f\r\n\r\n", uwb_triangle.d1, uwb_triangle.d2, uwb_triangle.d3);
  printf("COM11 ESTIMATED POSITION:\r\n  X: %.3f\r\n  Y: %.3f\r\n\r\n", uwb_triangle.x, uwb_triangle.y);

  float heading = calculateHeading();
  float distance = sqrtf(uwb_triangle.x * uwb_triangle.x + uwb_triangle.y * uwb_triangle.y);
  float x_cmd = (distance > 2.0f) ? 1.0f : 0.0f;
  float z_cmd = 0.0f;
  if (!(heading >= 340 || heading <= 20))
    z_cmd = (heading > 20 && heading <= 180) ? 0.8f : -0.9f;
  float y_cmd = 0.0f;

  printf("Required Action: x=%.2f, y=%.2f, z=%.2f\r\n", x_cmd, y_cmd, z_cmd);
}

void generateFollowCommand() {
  float heading = calculateHeading();
  float distance = sqrtf(uwb_triangle.x * uwb_triangle.x + uwb_triangle.y * uwb_triangle.y);
  float x = (distance > 2.0f) ? 1.0f : 0.0f;
  float z = 0.0f;
  if (!(heading >= 340 || heading <= 20))
    z = (heading > 20 && heading <= 180) ? 0.8f : -0.9f;
  float y = 0.0f;
  printf("Heading: %.1f deg, Distance: %.2f\r\n", heading, distance);
  printf("required action: x=%.2f, y=%.2f, z=%.2f\r\n", x, y, z);
}
